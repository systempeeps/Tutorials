#include <stdio.h>


int main(){

	float num1 = 10, num2 = 3.1;

	int sum = num1 + num2;
	int difference = num1 - num2;
	int product = num1 * num2;
	float quotient = num1 / num2;


	printf("Sum = %d\n", sum);
	printf("Difference = %d\n", difference);
	printf("Product = %d\n", product);
	printf("Quotient = %f\n", quotient);

	return 0;

}
